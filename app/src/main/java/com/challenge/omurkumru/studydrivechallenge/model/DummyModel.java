package com.challenge.omurkumru.studydrivechallenge.model;

public class DummyModel {

    String title;
    String detail;
    String author;

    public DummyModel(String title, String detail, String author) {
        this.title = title;
        this.detail = detail;
        this.author = author;
    }

    public String getTitle() {
        return title;
    }

    public String getDetail() {
        return detail;
    }

    public String getAuthor() {
        return author;
    }
}
